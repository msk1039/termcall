def lonlat_to_file_xy(lon, lat):
    x = int((lon + 180) / 360 * W) % W
    y = int((90 - lat) / 180 * H)
    if y >= H:
        y = H - 1
    if y < 0:
        y = 0
    return x, y


grid = [[" " for _ in range(W)] for _ in range(H)]
for lon, lat, marker in CITIES:
    fx, fy = lonlat_to_file_xy(lon, lat)
    if grid[fy][fx] == " ":
        grid[fy][fx] = marker

for row in grid:
    sys.stdout.write("".join(row) + "\n")
